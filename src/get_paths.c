#include "pipex.h"

char	**get_paths(char **env)
{
	int		i;

	i = 0;
	while(env[i])
	{
		if (ft_strncmp(env[i], "PATH", 4) == 0)
			break ;
		i++;
	}
	return (ft_split(env[i] + 5, ':'));
}